// Operadores Aritmeticos
let numeroA = 14;
let numeroB = 32;

// Suma +
let resultadoSuma = numeroA + numeroB;
console.log("El valor de la suma es: " + resultadoSuma);

// Resta -
let resultadoResta = numeroB - numeroA;
console.log("El valor de la resta es: " + resultadoResta);

// Multiplicación *
let resultadoMultiplicacion = numeroA * numeroB;
console.log("El valor de la multiplicación es: " + resultadoMultiplicacion);

// División /
let resultadoDivision = numeroB / numeroA;
console.log("El valor de la división es: " + resultadoDivision);

// Modulo %: Entrega el restante de una división
let Modulo = numeroB % numeroA;
console.log(Modulo);

// Exponenciación **
let numeroC = 2;
let numeroD = 3;
let resultadoExponenciacion = numeroC ** numeroD;
console.log("El valor de la exponenciación es: " + resultadoExponenciacion);

// Operación convinada
let operacionConbinada = 5 + 10 * 2;
console.log(operacionConbinada);

// Agrupación - Precedencias
// Los parentesis () tienen presedencias
let operacionConbinada2 = (5 + 10) * 2;
console.log(operacionConbinada2);

let operacionConbinada3 = 5 + (10 * 2);
console.log(operacionConbinada3);







