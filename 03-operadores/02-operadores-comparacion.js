// Operadores de comparación

//  De igualdad simple ==
console.log(5 == 5);
console.log(5 == "5");
console.log(5 == 15);
console.log("hola" == "Hola");
console.log("hola" == "hola");

//  De igualdad estricta ===, compara tambien el tipo de dato
console.log('\n');
console.log(12 === 12);
console.log(5 === "5");

// Mayor que >
console.log(4 > 2);
console.log(4 > 22);
console.log(4 > 4);

// Mayor o igual que >=
console.log('\n');
console.log(4 >= 4);

// Menor que <
console.log('\n');
console.log(4 < 2);
console.log(4 < 22);
console.log(4 < 4);

// Menor o igual que <=
console.log(4 <= 4);

// Distinto qué !=, simple: solo se fija en valor no en el tipo de dato
console.log('\n');
console.log('\n');
console.log('hola' != 'Hola');

// Distinto qué !==, complejo: no solo se fija en el valor, sino también en el tipo de dato
console.log('hola' !== 'Hola');
console.log(5 !== '5');
console.log(5 !== 5);
console.log("5" !== "5");

// Distinto qué !==, complejo: no solo se fija en el valor, sino también en el tipo de dato
console.log('\n');
console.log('hola' !== 'Hola');
console.log(5 !== '5');
console.log(5 !== 5);
console.log("5" !== "5");